package nanshen.constant;

/**
 * Time Constants
 *
 * @author WANG Minghao
 */
public class TimeConstants {

    public static final long SECONDS_IN_MILLISECONDS = 1000;
    public static final long MINUTE_IN_MILLISECONDS = 60 * SECONDS_IN_MILLISECONDS;
    public static final long HOUR_IN_MILLISECONDS = 60 * MINUTE_IN_MILLISECONDS;
    public static final long DAY_IN_MILLISECONDS = 24 * HOUR_IN_MILLISECONDS;
    public static final long MONTH_IN_MILLISECONDS = 30 * DAY_IN_MILLISECONDS;

    public static final int MINUTE_IN_SECONDS = 60;
    public static final int HOUR_IN_SECONDS = 3600;
    public static final int DAY_IN_SECONDS = 24 * HOUR_IN_SECONDS;
    public static final int MONTH_IN_SECONDS = 30 * DAY_IN_SECONDS;

}
